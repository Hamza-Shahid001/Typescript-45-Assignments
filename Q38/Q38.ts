// Cities


function describe_cities(city: string, country: string): void {
    console.log(`${city} is in "${country}".`) // message to printed on T-shirt
}
  
// Call the function with different arguments
describe_cities("Amsterdam", "Netherland"); // calling the function
describe_cities("Khatmandu", "Nepal"); // calling the function
describe_cities("Colombo", "Sri Lanka"); // calling the function
describe_cities("Gilgit","Pakistan"); // calling the function