// Large T-shirt

function make_shirt(size: string, message: string): void {
    console.log(`The is a ${size} size shirt and says "${message}".`) // message to printed on T-shirt
}
  
// Call the function with different arguments
make_shirt("Large", "I love Typescript."); // calling the function
make_shirt("Medium", "I love Typescript."); // calling the function
make_shirt("XXL", "Think Catrfully."); // calling the function
make_shirt("My size", "Think First before Act."); // calling the function