// T-shirt

function make_shirt(size: string, message: string): void {
    console.log(`The is a ${size} size shirt and says "${message}".`) // message to printed on T-shirt
}
  
// Call the function with different arguments
make_shirt("Large", "I am a Superhero."); // calling the function
make_shirt("Medium", "You are a Superhero."); // calling the function